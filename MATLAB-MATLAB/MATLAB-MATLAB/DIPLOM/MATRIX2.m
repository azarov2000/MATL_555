function [A2,A1,A0]=MATRIX2(z_C,N)

global beta zeta_e zeta_i Zeta_e Zeta_te nameS

[G00,G0s,Gr0,Grs]=GRIN(z_C,z_C,nameS);
[G002,G0s2,Gr02,Grs2]=GRIN(z_C+1/6,z_C+1/6,nameS);
Z=(zeta_e+zeta_i+Zeta_e);Zt=2*beta*Zeta_te;

O=zeros(2,2);E=eye(2,2);S=[0 1;-1 0];
A0=[E+2*zeta_i*N*G00*S, O, O, O;...
    2*zeta_i*N*Gr0*E,   E, O, O;...
    O, O, E+2*zeta_i*N*G002*S, O;...
    O, O, 2*zeta_i*N*Gr02*E, E];

A1=[2*Z*G00*E,  2*G0s*(-beta*N*E+Zt*S), O, O;...
    -2*Z*Gr0*S, 2*Grs*(Zt*E+beta*N*S), O, O;...
    O, O, 2*Z*G002*E,  2*G0s2*(-beta*N*E+Zt*S);...
    O, O, -2*Z*Gr02*S, 2*Grs2*(Zt*E+beta*N*S)];

A2=[G00*E,  G0s*beta*S, O, O;...
    -Gr0*S, beta*Grs*E, O, O;...
    O, O, G002*E,  G0s2*beta*S;...
    O, O, -Gr02*S, beta*Grs2*E];