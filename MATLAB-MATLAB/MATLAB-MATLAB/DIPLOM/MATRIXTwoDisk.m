function [A2,A1,A0]=MATRIXTwoDisk(z_C,N)

global beta zeta_e zeta_i Zeta_e Zeta_te nameS

[G00,G0s,Gr0,Grs]=GRIN(z_C,z_C,nameS);
[G002,G0s2,Gr02,Grs2]=GRIN(z_C,z_C+1/6,nameS);
[G003,G0s3,Gr03,Grs3]=GRIN(z_C+1/6,z_C+1/6,nameS);
[G004,G0s4,Gr04,Grs4]=GRIN(z_C+1/6,z_C,nameS);
Z=(zeta_e+zeta_i+Zeta_e);Zt=2*beta*Zeta_te;

O=zeros(2,2);E=eye(2,2);S=[0 1;-1 0];


A0=[E+2*zeta_i*N*G00*S, O, 2*zeta_i*N*G002*S,O;...
    2*zeta_i*N*Gr0*E,E,2*zeta_i*N*Gr02*E,O;...
    2*zeta_i*N*G004*S,O, E+2*zeta_i*N*G003*S,O;...
    2*zeta_i*N*Gr04*E,O,2*zeta_i*N*Gr03*E,E];

A1=[2*Z*G00*E,  2*G0s*(-beta*N*E+Zt*S), 2*Z*G002*E, 2*G0s2*(-beta*N*E+Zt*S);...
    -2*Z*Gr0*S, 2*Grs*(Zt*E+beta*N*S), -2*Z*Gr02*S, 2*Grs2*(Zt*E+beta*N*S);...
    2*Z*G004*E,  2*G0s4*(-beta*N*E+Zt*S), 2*Z*G003*E, 2*G0s3*(-beta*N*E+Zt*S);...
    -2*Z*Gr04*S, 2*Grs4*(Zt*E+beta*N*S), -2*Z*Gr03*S, 2*Grs3*(Zt*E+beta*N*S)];

A2=[G00*E,  G0s*beta*S, G002*E, G0s2*beta*S;...
    -Gr0*S, beta*Grs*E, -Gr02*S, beta*Grs2*E;...
    G004*E,  G0s4*beta*S, G003*E, G0s3*beta*S;...
    -Gr04*S, beta*Grs4*E, -Gr03*S, beta*Grs3*E];